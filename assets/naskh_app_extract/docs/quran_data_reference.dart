/// Get starting page for a Juz in Naskh mushaf
int getNaskhPageForJuz(int juz) => naskhPageForJuz[juz - 1];

/// Get starting page for a Surah in Naskh mushaf
int getNaskhPageForSura(int sura) => naskhPageForSura[sura - 1];

/// Get Juz number for a page in Naskh mushaf
int getNaskhJuzForPage(int page) {
  for (int i = naskhPageForJuz.length - 1; i >= 0; i--) {
    if (page >= naskhPageForJuz[i]) return i + 1;
  }
  return 1;
}

/// Get starting page for a Juz in Madani mushaf
int getMadaniPageForJuz(int juz) => madaniPageForJuz[juz - 1];

/// Get starting page for a Surah in Madani mushaf
